export const getAssetById = (id: string): Asset | undefined => {
  return MOCK_ASSETS.find((asset) => asset.id === id);
};

export const getAssetBySymbol = (symbol: string): Asset | undefined => {
  return MOCK_ASSETS.find((asset) => asset.symbol.toUpperCase() === symbol.toUpperCase());
};

export const getAssetsByType = (type: Asset['type']): Asset[] => {
  return MOCK_ASSETS.filter((asset) => asset.type === type);
};

export const getAssetsBySector = (sector: string): Asset[] => {
  return MOCK_ASSETS.filter((asset) => asset.sector === sector);
};

export const searchAssets = (query: string): Asset[] => {
  const lowerQuery = query.toLowerCase();
  return MOCK_ASSETS.filter(
    (asset) =>
      asset.symbol.toLowerCase().includes(lowerQuery) ||
      asset.name.toLowerCase().includes(lowerQuery) ||
      asset.sector.toLowerCase().includes(lowerQuery)
  );
};

